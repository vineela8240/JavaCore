package basicsjava;

public class PrimitiveDataTypes {
    byte b;
    short s;
    int i;
    long l;
    float f;
    double d;
    char ch;
    boolean bl;

    public static void main(String[] args) {
        PrimitiveDataTypes pt=new PrimitiveDataTypes();
        System.out.println(pt.b);
        System.out.println(pt.s);
        System.out.println(pt.i);
        System.out.println(pt.l);
        System.out.println(pt.f);
        System.out.println(pt.d);
        System.out.println(pt.ch);
        System.out.println(pt.bl);


    }

}
